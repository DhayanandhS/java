class Codechef
{
    
    public static void squareAndCube(int x) 
    {
        System.out.println(x * x);
        System.out.println(x * x * x);
    }
    
	public static void main (String[] args)
	{
        int a = 2;
        int b = 3;
        int c = 4;
        
        squareAndCube(a);
        squareAndCube(b);
        squareAndCube(c);
	}
}
